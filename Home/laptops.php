                <h2>macOS or Windows?</h2>
                <div class="whiteParagraphBox">
                    <p>While some students have a preference over Macs or PCs, when attending Saunders either platform will work for most of your courses. There are some specialized applications that students are required to use that will only work on Windows. All of the specialized applications are provided within our computer labs, so no matter which type of computer you own, we have you covered.</p>
                    <p>In most cases, you can install Windows on a Mac. In those cases, the Mac does require a bit more resources to run Windows smoothly.</p>
                </div>
            </section>
            <section class="grayMainBox">
                <h2>Processing Power</h2>
                <div class="whiteParagraphBox">
                    <p>Most Intel processors that are i5 or i7 will be more than enough processing power for Saunders students.</p>
                </div>
            </section>
            <section class="grayMainBox">
                <h2>Memory (RAM)</h2>
                <div class="whiteParagraphBox">
                    <p>8GB is the recommended minimum amount of RAM. If you are planning on installing Windows on a Mac, we strongly recommend 16GB of RAM as the minimum instead.</p>
                </div>
            </section>
            <section class="grayMainBox">
                <h2>Hard Drive</h2>
                <div class="whiteParagraphBox">
                    <p>RIT provides each student with access to storage via Google Drive, so a larger hard drive is not always necessary. We recommend a minimum of 256GB; however, if you plan to run virtual machines we recommend a minimum of 512GB.</p>
                    <p>Solid State Drives, or SSDs, will provide you far better performance than a traditional spinning disk. It is recommended that you consider getting an SSD and if you need more space, purchase an external drive.</p>
                </div>
            </section>
            <section class="grayMainBox">
                <h2>Tablet or Clamshell</h2>
                <div class="whiteParagraphBox">
                    <p>A traditional (clamshell) laptop will provide you with all the functionality you need; however, if you prefer to hand write notes in class and want to have them digitally available to you, take a look at a tablet style laptop.</p>
                </div>
            </section>
            <section class="grayMainBox">
                <h2>Getting Help</h2>
                <div class="whiteParagraphBox">
                    <p>Purchasing a new laptop can be difficult so give us a call at any time to discuss options and features. You may even consider reaching out to some of our student clubs to get in touch with current students to hear what works for them.</p>
                </div>
            </section>
