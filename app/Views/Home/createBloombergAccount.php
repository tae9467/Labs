                <h2>Creating an Account</h2>
                <div class="whiteParagraphBox">
                    <p>Start by launching the Bloomberg Software on the computer.</p>
                    <img src="/assets/img/bloomberg/pic1.png" alt="Image of Bloomberg 1.">
                    <p>Select one of the two screens and type <strong>LOGON</strong> and press <strong>Enter</strong> on your keyboard.</p>
                    <img src="/assets/img/bloomberg/pic2.png" alt="Image of Bloomberg 2.">
                    <p>This will bring you to the logon page.  From here, click on <strong>Create a New Login</strong>.</p>
                    <img src="/assets/img/bloomberg/pic3.png" alt="Image of Bloomberg 3.">
                    <p>Select your preferred language and be sure to change <strong>Are you creating a login for yourself?</strong> to <strong>Yes</strong>.</p>
                    <p>Select <strong>Yes</strong> for <strong>Is this a new Bloomberg user?</strong> </p>
                    <p>Click <strong>Continue</strong>. </p>
                    <img src="/assets/img/bloomberg/pic4.png" alt="Image of Bloomberg 4.">
                    <p>Enter your first and last name as well as your phone number.  We recommend using a cell phone as you will need to receive a pin to activate your account.  Using a cell phone allows for the pin to be texted to you.</p>
                    <p>Be sure to enter your RIT Email Address for the <strong>Corporate Email Address</strong>.</p>
                    <p>Click <strong>Continue</strong>.</p>
                    <img src="/assets/img/bloomberg/pic5.png" alt="Image of Bloomberg 5.">
                    <p>If you did not enter a cell phone number, you will be given an opportunity to give one on this screen to receive an SMS Text for your pin.</p>
                    <p>You may also select Automated Call.</p>
                    <p>Click <strong>Send Code</strong> once ready.</p>
                    <img src="/assets/img/bloomberg/pic6.png" alt="Image of Bloomberg 6.">
                    <p>Enter the code provided and click <strong>Continue</strong>.</p>
                    <img src="/assets/img/bloomberg/pic7.png" alt="Image of Bloomberg 7.">
                    <p>Enter in a password you wish to use for your Bloomberg Account twice.</p>
                    <p>Click <strong>Create Login</strong>.</p>
                    <img src="/assets/img/bloomberg/pic8.png" alt="Image of Bloomberg 8.">
                    <p>The last screen will give you your username along with the opportunity to change it.</p>
                    <p>Once you are ready, click on <strong>2) Click here to return to the login screen.</strong> and you will be all set.</p>
